class CollectionsTBVDataSource: NSObject, UITableViewDataSource {
    
    var collections = CollectionsManager.sharedManager.collections
    
    func numberOfSections(in tableView: UITableView) -> Int {
        return 1
    }
    
    func tableView(_ tableView: UITableView, numberOfRowsInSection section: Int) -> Int {
        return collections.count
    }
    
    func tableView(_ tableView: UITableView, cellForRowAt indexPath: IndexPath) -> UITableViewCell {
         let cell = tableView.dequeueReusableCell(withIdentifier: collectionsTBVCellID, for: indexPath) as! CollectionsTBVCell
        
        cell.backgroundColor = UIColor.lightGray
        
        cell.nameLabel.text = collections[indexPath.row].name
        
        return cell
    }
}

